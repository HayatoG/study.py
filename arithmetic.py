# + -Soma
n1 = 10
n2 = 2
soma = n1 + n2

print("Soma:",soma)

# - -Subtração
subt = n1 - n2

print("Subtração:",subt)

# * -Multiplicação
mult = n1 * n2

print("Multiplicação:",mult)

# / -Divisão
div = n1/n2

print("Divisão:",div)

# ** -Exponenciação
exp = n1**n2

print("Exponenciação:",exp)

# % -Módulo
mod = (n1%(n2+1))

print("Resto da divisão:", mod)